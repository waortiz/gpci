public class ReporteIntegranteProyecto {

    private int anyoCreacion;
    private String nombreCortoProyecto;
    private String codigo;
    private String tipoIntegrante;
    private String numeroIdentificacionIntegrante;
    private String integrante;
    private String rolIntegrante;

    /**
     * @return the anyoCreacion
     */
    public int getAnyoCreacion() {
        return anyoCreacion;
    }

    /**
     * @param anyoCreacion the anyoCreacion to set
     */
    public void setAnyoCreacion(int anyoCreacion) {
        this.anyoCreacion = anyoCreacion;
    }

    /**
     * @return the nombreCortoProyecto
     */
    public String getNombreCortoProyecto() {
        return nombreCortoProyecto;
    }

    /**
     * @param nombreCortoProyecto the nombreCortoProyecto to set
     */
    public void setNombreCortoProyecto(String nombreCortoProyecto) {
        this.nombreCortoProyecto = nombreCortoProyecto;
    }

    /**
     * @return the codigo
     */
    public String getCodigo() {
        return codigo;
    }

    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    /**
     * @return the tipoIntegrante
     */
    public String getTipoIntegrante() {
        return tipoIntegrante;
    }

    /**
     * @param tipoIntegrante the tipoIntegrante to set
     */
    public void setTipoIntegrante(String tipoIntegrante) {
        this.tipoIntegrante = tipoIntegrante;
    }

    /**
     * @return the numeroIdentificacionIntegrante
     */
    public String getNumeroIdentificacionIntegrante() {
        return numeroIdentificacionIntegrante;
    }

    /**
     * @param numeroIdentificacionIntegrante the numeroIdentificacionIntegrante to set
     */
    public void setNumeroIdentificacionIntegrante(String numeroIdentificacionIntegrante) {
        this.numeroIdentificacionIntegrante = numeroIdentificacionIntegrante;
    }

    /**
     * @return the integrante
     */
    public String getIntegrante() {
        return integrante;
    }

    /**
     * @param integrante the integrante to set
     */
    public void setIntegrante(String integrante) {
        this.integrante = integrante;
    }

    /**
     * @return the rolIntegrante
     */
    public String getRolIntegrante() {
        return rolIntegrante;
    }

    /**
     * @param rolIntegrante the rolIntegrante to set
     */
    public void setRolIntegrante(String rolIntegrante) {
        this.rolIntegrante = rolIntegrante;
    }
    
    

}
